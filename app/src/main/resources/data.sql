CREATE TABLE CUSTOMER (
  CUSTOMER_ID INT AUTO_INCREMENT  PRIMARY KEY,
  ACCOUNT_NO  INT NOT NULL,
  NAME VARCHAR(250) NOT NULL,
  PASSWORD VARCHAR(250) NOT NULL
  );

